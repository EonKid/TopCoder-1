class MovingRooksDiv1 {
    public:
    vector<int> move(vector<int> Y1, vector<int> Y2) {
		vector<int> ret;
		int n = Y1.size();

		for (int i = 0; i < n; i++) {
			if (Y1[i] < Y2[i]) return vector<int>{ -1 };
			if (Y1[i] == Y2[i]) continue;
			for (int j = i + 1; j < n; j++) {
				if (Y1[i] < Y1[j]) continue;	//can not move by definition
				if (Y1[j] < Y2[i]) continue;	//if moving like this then destination won't be reached
				swap(Y1[i], Y1[j]);
				if (ret.size() < 2500) {
					ret.push_back(i);
					ret.push_back(j);
				}
			}
			if (Y1[i] != Y2[i]) return vector<int> {-1};
		}

		return ret;
    }
};

